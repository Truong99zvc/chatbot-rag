"""
tests/evaluation/generate_answers.py
=====================================
Step 1 of RAGAS evaluation pipeline.

Runs the RAG pipeline for every question in eval_dataset.json and captures:
  - answer      : text generated by the LLM
  - contexts    : raw text of the retrieved chunks (needed by RAGAS)
  - ground_truth: reference answer from the dataset

Output is saved to tests/evaluation/evaluated_answers.json.

Usage:
    uv run python tests/evaluation/generate_answers.py
    # or
    make generate-eval-answers
"""
from __future__ import annotations

import asyncio
import json
import logging
import sys
import time
from pathlib import Path

# ---------------------------------------------------------------------------
# Ensure project root is on sys.path
# ---------------------------------------------------------------------------
PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

from dotenv import load_dotenv
load_dotenv(PROJECT_ROOT / ".env")

from app.config.settings import settings
from app.embeddings.embedder import get_embeddings
from app.rag.generator import Generator
from app.rag.prompt_builder import build_memory_context, build_rag_prompt
from app.rag.retriever import Retriever
from app.vectorstore.vector_db import VectorDB

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(message)s",
    datefmt="%H:%M:%S",
)
logger = logging.getLogger("generate_answers")

EVAL_DATASET = Path(__file__).parent / "eval_dataset.json"
OUTPUT_FILE = Path(__file__).parent / "evaluated_answers.json"


def load_eval_dataset() -> list[dict]:
    with open(EVAL_DATASET, encoding="utf-8") as f:
        return json.load(f)


def build_rag_components() -> tuple[Retriever, Generator]:
    """Load FAISS index and initialize retriever + generator."""
    embeddings = get_embeddings()
    db = VectorDB(embeddings)
    try:
        vector_store = db.load()
    except FileNotFoundError:
        logger.error(
            "FAISS index not found at '%s'. Run `make build-index` first.",
            settings.FAISS_INDEX_DIR,
        )
        sys.exit(1)
    return Retriever(vector_store), Generator()


def answer_one(
    question: str,
    retriever: Retriever,
    generator: Generator,
    prompt,
) -> tuple[str, list[str]]:
    """
    Run the RAG pipeline for a single question.

    Returns:
        (answer_text, list_of_context_strings)
    """
    docs = retriever.retrieve(question, k=settings.TOP_K_RESULTS)
    if not docs:
        return "Không tìm thấy thông tin liên quan.", []

    context = retriever.format_context(docs)
    memory_context = build_memory_context([])  # no history for eval

    answer = generator.generate(
        prompt,
        context=context,
        question=question,
        memory_context=memory_context,
    )
    # Extract raw page content for RAGAS (not the formatted markdown)
    contexts = [doc.page_content for doc in docs]
    return answer, contexts


def main() -> None:
    dataset = load_eval_dataset()
    logger.info("Loaded %d questions from eval dataset.", len(dataset))

    retriever, generator = build_rag_components()
    prompt = build_rag_prompt()

    results: list[dict] = []
    start = time.perf_counter()

    for i, item in enumerate(dataset, start=1):
        question = item["question"]
        ground_truth = item.get("ground_truth", "")
        topic = item.get("topic", "unknown")

        logger.info("[%d/%d] (%s) %s", i, len(dataset), topic, question[:60])
        t0 = time.perf_counter()

        answer, contexts = answer_one(question, retriever, generator, prompt)
        elapsed = time.perf_counter() - t0

        logger.info("  → answered in %.1fs (%d contexts)", elapsed, len(contexts))

        results.append(
            {
                "id": item.get("id", f"q{i:02d}"),
                "topic": topic,
                "question": question,
                "answer": answer,
                "contexts": contexts,
                "ground_truth": ground_truth,
            }
        )

    # Save results
    OUTPUT_FILE.write_text(
        json.dumps(results, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )

    total = time.perf_counter() - start
    logger.info("=" * 60)
    logger.info("✅ Done! Generated %d answers in %.1fs", len(results), total)
    logger.info("   Output: %s", OUTPUT_FILE)
    logger.info("   Next:   uv run python tests/evaluation/ragas_eval.py")


if __name__ == "__main__":
    main()
